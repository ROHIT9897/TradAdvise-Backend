# ── Quick lookup by ticker ────────────────────────────────
NSE_TICKER_MAP = {
    ticker: {"name": name, "sector": sector}
    for ticker, name, sector in NSE_STOCKS
}

# ── Get all tickers (for market movers) ──────────────────
def get_all_tickers() -> list[str]:
    return [ticker for ticker, _, _ in NSE_STOCKS]

# ── Get company name ──────────────────────────────────────
def get_company_name(ticker: str) -> str:
    info = NSE_TICKER_MAP.get(ticker, {})
    return info.get("name", ticker)

# ── Get sector ────────────────────────────────────────────
def get_sector(ticker: str) -> str:
    info = NSE_TICKER_MAP.get(ticker, {})
    return info.get("sector", "Unknown")

# ── Search stocks ─────────────────────────────────────────
def search_stocks(query: str, limit: int = 10) -> list[dict]:
    query = query.upper().strip()
    results = []
    for ticker, name, sector in NSE_STOCKS:
        if query in ticker or query in name.upper():
            results.append({
                "ticker": ticker,
                "name":   name,
                "sector": sector
            })
    # Prefix match first
    results.sort(key=lambda x: (
        0 if x["ticker"].startswith(query) else 1
    ))
    return results[:limit]

